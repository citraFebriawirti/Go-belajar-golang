package main

import "fmt"

func main(){
	var var1 = 50
	var var2 = 20

	var add = var1 + var2
	fmt.Println("Hasil Tambah :",add)

	var sub = var1 - var2
	fmt.Println("Hasil Kurang :",sub)

	var mul = var1 * var2
	fmt.Println("Hasil Kali :",mul)

	var div = var1 / var2
	fmt.Println("Hasil Bagi :",div)
}